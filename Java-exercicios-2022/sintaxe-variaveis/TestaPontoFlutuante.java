public class TestaPontoFlutuante {
    public static void main(String[] args) {
        double salario = 1290.70;

        System.out.println("Meu salário é: " + salario);

        double PI = 3.14;

        double divisao = PI / 2;

        System.out.println("Resultado da divisão 1: " + divisao);

        // 5.0 e 2.0 são obrigatórios para a divisão correta.
        double Outradivisao = 5.0 / 2.0;

        System.out.println("Resultado da divisão 2: " + Outradivisao);
    }
}
